s = input("digete algo:")
print("o tipo primitivo do valor é", (type(s)))
print("so tem espaço? ", s.isspace())
print("é um número?", s.isnumeric())
print("é alphabetico?", s.isalpha())
print("é alphanumerico?", s.isalnum())
print("Está em maiusculo?", s.isupper())
print("Está em menusculo?", s.islower())
print("Está capitalizado?", s.istitle())




